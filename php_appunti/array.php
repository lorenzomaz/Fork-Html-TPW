$cars = ['bmw', 'mercedes', 'audi', 'ford'];

/* -------------------------------------------------------------------------- */
/*                                  CON WHILE                                 */
/* -------------------------------------------------------------------------- */
$i=0; //inizializzo contatore
while($i < count($cars))
{
	echo $cars[$i].'<br>';
	$i++; //incremento contatore
}

/* -------------------------------------------------------------------------- */
/*                                CON CICLO FOR                               */
/* -------------------------------------------------------------------------- */

for($i = 0;$i < count($cars);$i++)
	echo $cars[$i].'<br>';


/* -------------------------------------------------------------------------- */
/*                                  CON FOREACH                               */
/* -------------------------------------------------------------------------- */

foreach($cars as $car)
	echo $car.'<br>';


/* -------------------------------------------------------------------------- */
/*                        ARRAY ASSOCIATIVI CON FOREACH                       */
/* -------------------------------------------------------------------------- */


$age=array(
    "Peter"=>"35",
    "Ben"=>"37",
    "Joe"=>"43",
    "Mary"=>"32"
);

foreach($age as $key => $value) {
    echo "Key=" . $key . ", Value=" . $value;
    echo "<br>";
  }


